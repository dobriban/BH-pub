#!/usr/bin/env python3
"""Exact count-thinning Monte Carlo for the 163N+N+3N Gaussian block model.

For each alpha, conditional on Z, the block counts below a decreasing sequence
of BH thresholds are generated by exact binomial thinning.  Iterating from
R^(0)=m reaches the greatest fixed point of r -> #{P_i <= alpha r/m}, hence the
ordinary BH rejection count, without generating or sorting all m p-values.
"""
from __future__ import annotations
import argparse,csv,json,math,os,time
from pathlib import Path
import numpy as np
from scipy.special import ndtr, ndtri

BLOCKS=np.array([163,1,3],dtype=np.int64)
MU=np.array([0.0,37/20,59/12],dtype=float)
RHO=np.array([3/10,2/11,20/21],dtype=float)
SD=np.sqrt(1-RHO*RHO)
ALPHAS=np.arange(1,101,dtype=float)/1000

def block_cdf(t:np.ndarray,z:np.ndarray)->np.ndarray:
    """Conditional CDFs P(P_g <= t | Z=z), shape (len(z),3)."""
    t=np.asarray(t,dtype=float)
    z=np.asarray(z,dtype=float)
    # Avoid ndtri(1) at t=0; these rows necessarily have zero retained counts.
    c=np.empty_like(t)
    pos=t>0
    c[~pos]=np.inf
    c[pos]=ndtri(1-t[pos]/2)
    mean=MU[None,:]+z[:,None]*RHO[None,:]
    cc=c[:,None]
    return ndtr((-cc-mean)/SD)+ndtr((mean-cc)/SD)

def run_task(task:tuple[int,int,float,int,int,int])->dict:
    N,index,alpha,B,batch,seed=task
    rng=np.random.default_rng(seed)
    sizes=BLOCKS*N
    m=int(sizes.sum())
    total=total2=0.0
    rejections=0
    iterations_sum=0
    iterations_max=0
    done=0
    while done<B:
        b=min(batch,B-done)
        z=rng.standard_normal(b)
        t_old=np.full(b,alpha,dtype=float)
        probs=block_cdf(t_old,z)
        counts=np.column_stack([
            rng.binomial(int(sizes[g]),probs[:,g]) for g in range(3)
        ]).astype(np.int64,copy=False)
        R=counts.sum(axis=1)
        nit=np.ones(b,dtype=np.int32)
        active=np.ones(b,dtype=bool)
        while active.any():
            idx=np.flatnonzero(active)
            t_new=alpha*R[idx]/m
            fixed=(R[idx]==0)|(t_new==t_old[idx])
            if fixed.any():
                active[idx[fixed]]=False
            live=idx[~fixed]
            if live.size==0:
                break
            new_t=t_new[~fixed]
            p_new=block_cdf(new_t,z[live])
            p_old=block_cdf(t_old[live],z[live])
            ratio=np.divide(p_new,p_old,out=np.zeros_like(p_new),where=p_old>0)
            np.clip(ratio,0.0,1.0,out=ratio)
            new_counts=np.column_stack([
                rng.binomial(counts[live,g],ratio[:,g]) for g in range(3)
            ]).astype(np.int64,copy=False)
            R_new=new_counts.sum(axis=1)
            stable=R_new==R[live]
            counts[live]=new_counts
            R[live]=R_new
            t_old[live]=new_t
            nit[live]+=1
            if stable.any():
                active[live[stable]]=False
        fdp=np.divide(counts[:,0],R,out=np.zeros(b,dtype=float),where=R>0)
        total+=float(fdp.sum())
        total2+=float(np.dot(fdp,fdp))
        rejections+=int(np.count_nonzero(R))
        iterations_sum+=int(nit.sum())
        iterations_max=max(iterations_max,int(nit.max()))
        done+=b
    mean=total/B
    var=max((total2-B*mean*mean)/(B-1),0.0)
    mcse=math.sqrt(var/B)
    rp=rejections/B
    rpse=math.sqrt(rp*(1-rp)/B)
    return {
        'N':N,'m':m,'alpha_index':index,'alpha':alpha,'replications':B,
        'batch':batch,'seed':seed,'fdr':mean,'mcse':mcse,
        'ci95_lower':mean-1.959963984540054*mcse,
        'ci95_upper':mean+1.959963984540054*mcse,
        'rejection_probability':rp,'rejection_probability_mcse':rpse,
        'mean_fixed_point_iterations':iterations_sum/B,
        'maximum_fixed_point_iterations':iterations_max,
    }

def main():
    p=argparse.ArgumentParser()
    p.add_argument('--output-dir',type=Path,required=True)
    p.add_argument('--replications',type=int,default=100000)
    p.add_argument('--batch',type=int,default=20000)
    p.add_argument('--Ns',type=int,nargs='+',default=[50,100,500,1000])
    p.add_argument('--seed',type=int,default=20260821)
    args=p.parse_args()
    if args.replications < 2:
        p.error('--replications must be at least 2')
    if args.batch <= 0:
        p.error('--batch must be positive')
    if any(N <= 0 for N in args.Ns):
        p.error('all --Ns values must be positive')
    args.output_dir.mkdir(parents=True,exist_ok=True)
    tasks=[]
    for N in args.Ns:
        for j,alpha in enumerate(ALPHAS):
            seed=args.seed+100003*N+7919*j
            tasks.append((N,j,float(alpha),args.replications,args.batch,seed))
    t0=time.time()
    rows=[]
    for k,task in enumerate(tasks,1):
        rows.append(run_task(task))
        if k%10==0:
            print(f'completed {k}/{len(tasks)} elapsed={time.time()-t0:.1f}s',flush=True)
    rows.sort(key=lambda r:(r['N'],r['alpha']))
    csvp=args.output_dir/'finite_sample_fdr_curve.csv'
    with csvp.open('w',newline='') as f:
        w=csv.DictWriter(f,fieldnames=list(rows[0].keys()))
        w.writeheader();w.writerows(rows)
    payload={
      'model':{
        'block_sizes_per_N':[163,1,3],
        'means':['0','37/20','59/12'],
        'loadings':['3/10','2/11','20/21'],
        'residual_sds':['sqrt(91)/10','3*sqrt(13)/11','sqrt(41)/21'],
      },
      'method':'exact conditional binomial thinning to the greatest BH fixed point',
      'alpha_grid':{'start':0.001,'stop':0.100,'step':0.001},
      'Ns':args.Ns,'replications_per_N_alpha':args.replications,
      'batch':args.batch,'base_seed':args.seed,
      'elapsed_seconds':time.time()-t0,'rows':rows,
    }
    jsonp=args.output_dir/'finite_sample_fdr_curve.json'
    jsonp.write_text(json.dumps(payload,indent=2)+'\n')
    print('wrote',csvp,'and',jsonp,'elapsed',time.time()-t0,flush=True)
    for N in args.Ns:
        rr={round(r['alpha'],3):r for r in rows if r['N']==N}
        print('N',N,'m',167*N,
              [(a,rr[a]['fdr'],rr[a]['mcse']) for a in (0.01,0.05,0.1)],flush=True)
if __name__=='__main__':
    main()
